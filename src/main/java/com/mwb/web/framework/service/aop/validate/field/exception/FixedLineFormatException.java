package com.mwb.web.framework.service.aop.validate.field.exception;

public class FixedLineFormatException extends ValidationException {
	
	private static final long serialVersionUID = 1L;

	public FixedLineFormatException() {
		super();
	}

	public FixedLineFormatException(String message, Throwable cause) {
		super(message, cause);
	}

	public FixedLineFormatException(String message) {
		super(message);
	}

	public FixedLineFormatException(Throwable cause) {
		super(cause);
	}
}
