package com.mwb.web.framework.service.aop.validate.field.exception;

public class MobileFormatException extends ValidationException {
	
	private static final long serialVersionUID = 1L;

	public MobileFormatException() {
		super();
	}

	public MobileFormatException(String message, Throwable cause) {
		super(message, cause);
	}

	public MobileFormatException(String message) {
		super(message);
	}

	public MobileFormatException(Throwable cause) {
		super(cause);
	}
}
